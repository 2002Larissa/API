package br.com.projeto.api.modelo;

public class Pessoa {
    
    //Atributos
    private String nome;
    private int idade;

    //Get e Set
    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
    public int getIdade() {
        return idade;
    }
    public void setIdade(int idade) {
        this.idade = idade;
    }
}
